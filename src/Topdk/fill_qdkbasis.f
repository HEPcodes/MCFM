      subroutine fill_qdkbasis(i1,i2,
     . qdkbasise0,qdkbasise1,qdkbasis0,qdkbasis1,
     . qdkbasis2,qdkbasis3,qdkbasis4,qdkbasis5,qdkbasis6)
      implicit none
      include 'constants.f'
      include 'virtexp.f'
      include 'sprods_com.f'
      integer i1,i2
      double precision
     . qdkbasise0,qdkbasise1,qdkbasis0,qdkbasis1,
     . qdkbasis2,qdkbasis3,qdkbasis4,qdkbasis5,qdkbasis6

      qdkbasise0=  + ro * (  - 2.D0*S(i1,5)*S(i1,6) - 2.D0*S(i1,5)*S(i2
     &    ,6) - 2.D0*S(i1,6)*S(i2,5) - 2.D0*S(i2,5)*S(i2,6) - 4.D0*S(1,
     &    2)*S(5,6)*t1 + 4.D0*S(1,2)*S(5,6)*t1**2 + 4.D0*S(1,5)*S(2,6)
     &     - 4.D0*S(1,5)*S(2,6)*t1 + 4.D0*S(1,6)*S(2,5)*t1 - 2.D0*S(3,5
     &    )*S(i1,6) - 2.D0*S(3,5)*S(i2,6) - 4.D0*S(3,5)*S(3,6) - 2.D0*
     &    S(3,6)*S(i1,5) - 2.D0*S(3,6)*S(i2,5) )
      qdkbasise0 = qdkbasise0 + ro**2 * ( S(1,2)*S(5,6) )
      qdkbasise0 = qdkbasise0 - 8.D0*S(3,5)*S(i1,6) + 16.D0*S(3,5)*S(i1
     &    ,6)*t1 - 16.D0*S(3,5)*S(i1,6)*t1**2 - 8.D0*S(3,5)*S(i2,6) + 
     &    16.D0*S(3,5)*S(i2,6)*t1 - 16.D0*S(3,5)*S(i2,6)*t1**2 - 8.D0*
     &    S(3,5)*S(3,6) + 16.D0*S(3,5)*S(3,6)*t1 - 16.D0*S(3,5)*S(3,6)*
     &    t1**2

      qdkbasise1=  + ro * (  - 2.D0*S(1,2)*S(5,6) + 2.D0*S(1,5)*S(1,6)
     &     + 2.D0*S(1,5)*S(2,6) + 2.D0*S(1,6)*S(2,5) + 2.D0*S(2,5)*S(2,
     &    6) )
      qdkbasise1 = qdkbasise1 + 8.D0*S(3,5)*S(i1,6) + 8.D0*S(3,5)*S(i2,
     &    6) + 8.D0*S(3,5)*S(3,6)

      qdkbasis0=  + ro * (  - 2.D0*S(i1,5)*S(i1,6) + 2.D0*S(i1,5)*S(i2,
     &    6) - 4.D0*S(i1,5)*S(i2,6)*t1 - 2.D0*S(i1,6)*S(i2,5) + 4.D0*S(
     &    i1,6)*S(i2,5)*t1 - 2.D0*S(i2,5)*S(i2,6) - 2.D0*S(3,5)*S(i1,6)
     &     - 2.D0*S(3,5)*S(i2,6) - 4.D0*S(3,5)*S(3,6) - 2.D0*S(3,6)*S(
     &    i1,5) - 2.D0*S(3,6)*S(i2,5) - 4.D0*S(5,6)*S(i1,i2)*t1 + 4.D0*
     &    S(5,6)*S(i1,i2)*t1**2 )
      qdkbasis0 = qdkbasis0 + ro**2 * ( S(5,6)*S(i1,i2) )
      qdkbasis0 = qdkbasis0 - 8.D0*S(3,5)*S(i1,6) + 16.D0*S(3,5)*S(i1,6
     &    )*t1 - 16.D0*S(3,5)*S(i1,6)*t1**2 - 8.D0*S(3,5)*S(i2,6) + 16.D
     &    0*S(3,5)*S(i2,6)*t1 - 16.D0*S(3,5)*S(i2,6)*t1**2 - 8.D0*S(3,5
     &    )*S(3,6) + 16.D0*S(3,5)*S(3,6)*t1 - 16.D0*S(3,5)*S(3,6)*t1**2

      qdkbasis1=  + ro * (  - 1.D0/2.D0*S(i1,5)*S(i1,6) + 1.D0/2.D0*S(
     &    i1,5)*S(i1,6)*t1 + 1.D0/2.D0*S(i1,5)*S(i2,6) - 5.D0/2.D0*S(i1
     &    ,5)*S(i2,6)*t1 + 2.D0*S(i1,5)*S(i2,6)*t1**2 + 3.D0/2.D0*S(i1,
     &    6)*S(i2,5)*t1 - 2.D0*S(i1,6)*S(i2,5)*t1**2 + 1.D0/2.D0*S(i2,5
     &    )*S(i2,6)*t1 - S(3,5)*S(i1,6) + 3.D0/2.D0*S(3,5)*S(i1,6)*t1
     &     - 1.D0/2.D0*S(3,5)*S(i2,6) + 3.D0/2.D0*S(3,5)*S(i2,6)*t1 - 
     &    S(3,5)*S(3,6) + 2.D0*S(3,5)*S(3,6)*t1 - 1.D0/2.D0*S(3,6)*S(i1
     &    ,5) + 1.D0/2.D0*S(3,6)*S(i1,5)*t1 + 1.D0/2.D0*S(3,6)*S(i2,5)*
     &    t1 - S(5,6)*S(i1,i2)*t1 + 3.D0*S(5,6)*S(i1,i2)*t1**2 - 2.D0*
     &    S(5,6)*S(i1,i2)*t1**3 )
      qdkbasis1 = qdkbasis1 + ro**2 * ( 1.D0/4.D0*S(i1,5)*S(i2,6) - 1.D0
     &    /4.D0*S(i1,6)*S(i2,5) + 1.D0/4.D0*S(5,6)*S(i1,i2) - 1.D0/2.D0
     &    *S(5,6)*S(i1,i2)*t1 )
      qdkbasis1 = qdkbasis1 + 4.D0*S(3,5)*S(i1,6)*t1 - 12.D0*S(3,5)*S(
     &    i1,6)*t1**2 + 8.D0*S(3,5)*S(i1,6)*t1**3 + 4.D0*S(3,5)*S(i2,6)
     &    *t1 - 12.D0*S(3,5)*S(i2,6)*t1**2 + 8.D0*S(3,5)*S(i2,6)*t1**3
     &     + 4.D0*S(3,5)*S(3,6)*t1 - 12.D0*S(3,5)*S(3,6)*t1**2 + 8.D0*
     &    S(3,5)*S(3,6)*t1**3

      qdkbasis2=  + ro * ( 2.D0*S(i1,5)*S(i1,6)*t1 + 2.D0*S(i1,5)*S(i1,
     &    6)*t1**2 - 2.D0*S(i1,5)*S(i2,6)*t1 + 6.D0*S(i1,5)*S(i2,6)*
     &    t1**2 - 2.D0*S(i1,6)*S(i2,5)*t1**2 + 2.D0*S(i2,5)*S(i2,6)*
     &    t1**2 + 4.D0*S(3,5)*S(i1,6)*t1 - 2.D0*S(3,5)*S(i1,6)*t1**2 + 
     &    2.D0*S(3,5)*S(i2,6)*t1 - 2.D0*S(3,5)*S(i2,6)*t1**2 + 4.D0*S(3
     &    ,5)*S(3,6)*t1 + 2.D0*S(3,6)*S(i1,5)*t1 + 2.D0*S(3,6)*S(i1,5)*
     &    t1**2 + 2.D0*S(3,6)*S(i2,5)*t1**2 + 4.D0*S(5,6)*S(i1,i2)*
     &    t1**2 - 4.D0*S(5,6)*S(i1,i2)*t1**3 )
      qdkbasis2 = qdkbasis2 + ro**2 * (  - S(i1,5)*S(i2,6)*t1 + S(i1,6)
     &    *S(i2,5)*t1 - S(5,6)*S(i1,i2)*t1 )
      qdkbasis2 = qdkbasis2 + 16.D0*S(3,5)*S(i1,6)*t1**3 + 16.D0*S(3,5)
     &    *S(i2,6)*t1**3 + 16.D0*S(3,5)*S(3,6)*t1**3

      qdkbasis3=  + ro * (  - 20.D0*S(i1,5)*S(i1,6) - 12.D0*S(i1,5)*S(
     &    i1,6)*t1 + 20.D0*S(i1,5)*S(i2,6) - 52.D0*S(i1,5)*S(i2,6)*t1
     &     - 8.D0*S(i1,6)*S(i2,5) + 28.D0*S(i1,6)*S(i2,5)*t1 - 8.D0*S(
     &    i2,5)*S(i2,6) - 12.D0*S(i2,5)*S(i2,6)*t1 - 32.D0*S(3,5)*S(i1,
     &    6) + 12.D0*S(3,5)*S(i1,6)*t1 - 20.D0*S(3,5)*S(i2,6) + 12.D0*
     &    S(3,5)*S(i2,6)*t1 - 40.D0*S(3,5)*S(3,6) - 20.D0*S(3,6)*S(i1,5
     &    ) - 12.D0*S(3,6)*S(i1,5)*t1 - 8.D0*S(3,6)*S(i2,5) - 12.D0*S(3
     &    ,6)*S(i2,5)*t1 - 40.D0*S(5,6)*S(i1,i2)*t1 + 40.D0*S(5,6)*S(i1
     &    ,i2)*t1**2 )
      qdkbasis3 = qdkbasis3 + ro**2 * ( 6.D0*S(i1,5)*S(i2,6) - 6.D0*S(
     &    i1,6)*S(i2,5) + 10.D0*S(5,6)*S(i1,i2) )
      qdkbasis3 = qdkbasis3 - 32.D0*S(3,5)*S(i1,6) + 64.D0*S(3,5)*S(i1,
     &    6)*t1 - 160.D0*S(3,5)*S(i1,6)*t1**2 - 32.D0*S(3,5)*S(i2,6) + 
     &    64.D0*S(3,5)*S(i2,6)*t1 - 160.D0*S(3,5)*S(i2,6)*t1**2 - 32.D0
     &    *S(3,5)*S(3,6) + 64.D0*S(3,5)*S(3,6)*t1 - 160.D0*S(3,5)*S(3,6
     &    )*t1**2

      qdkbasis4=  + ro * (  - 1.D0/2.D0*S(i1,5)*S(i1,6) + 3.D0/2.D0*S(
     &    i1,5)*S(i1,6)*t1 - S(i1,5)*S(i1,6)*t1**2 - 1.D0/2.D0*S(i1,5)*
     &    S(i2,6) + 3.D0/2.D0*S(i1,5)*S(i2,6)*t1 - S(i1,5)*S(i2,6)*
     &    t1**2 + 1.D0/2.D0*S(i1,6)*S(i2,5)*t1 - S(i1,6)*S(i2,5)*t1**2
     &     + 1.D0/2.D0*S(i2,5)*S(i2,6)*t1 - S(i2,5)*S(i2,6)*t1**2 - S(3
     &    ,5)*S(i1,6) + 7.D0/2.D0*S(3,5)*S(i1,6)*t1 - 3.D0*S(3,5)*S(i1,
     &    6)*t1**2 - 1.D0/2.D0*S(3,5)*S(i2,6) + 5.D0/2.D0*S(3,5)*S(i2,6
     &    )*t1 - 3.D0*S(3,5)*S(i2,6)*t1**2 - S(3,5)*S(3,6) + 4.D0*S(3,5
     &    )*S(3,6)*t1 - 4.D0*S(3,5)*S(3,6)*t1**2 - 1.D0/2.D0*S(3,6)*S(
     &    i1,5) + 3.D0/2.D0*S(3,6)*S(i1,5)*t1 - S(3,6)*S(i1,5)*t1**2 + 
     &    1.D0/2.D0*S(3,6)*S(i2,5)*t1 - S(3,6)*S(i2,5)*t1**2 )
      qdkbasis4 = qdkbasis4 + ro**2 * (  - 1.D0/4.D0*S(i1,5)*S(i1,6) - 
     &    1.D0/2.D0*S(i1,5)*S(i2,6)*t1 - 1.D0/2.D0*S(i1,6)*S(i2,5) + 1.D
     &    0/2.D0*S(i1,6)*S(i2,5)*t1 - 1.D0/4.D0*S(i2,5)*S(i2,6) - 1.D0/
     &    2.D0*S(3,5)*S(i1,6) - 1.D0/2.D0*S(3,5)*S(i2,6) - S(3,5)*S(3,6
     &    ) - 1.D0/2.D0*S(3,6)*S(i1,5) - 1.D0/2.D0*S(3,6)*S(i2,5) - S(5
     &    ,6)*S(i1,i2)*t1 + S(5,6)*S(i1,i2)*t1**2 )
      qdkbasis4 = qdkbasis4 + ro**3 * ( 1.D0/4.D0*S(5,6)*S(i1,i2) )

      qdkbasis5=  + ro * ( S(i1,5)*S(i1,6) - 2.D0*S(i1,5)*S(i1,6)*t1 + 
     &    S(i1,5)*S(i1,6)*t1**2 + S(i1,5)*S(i2,6) - 2.D0*S(i1,5)*S(i2,6
     &    )*t1 + S(i1,5)*S(i2,6)*t1**2 - S(i1,6)*S(i2,5)*t1 + S(i1,6)*
     &    S(i2,5)*t1**2 - S(i2,5)*S(i2,6)*t1 + S(i2,5)*S(i2,6)*t1**2 - 
     &    S(3,5)*S(i1,6) - S(3,5)*S(i1,6)*t1**2 - S(3,5)*S(i2,6) + S(3,
     &    5)*S(i2,6)*t1 - S(3,5)*S(i2,6)*t1**2 - S(3,5)*S(3,6) + S(3,6)
     &    *S(i1,5) - 2.D0*S(3,6)*S(i1,5)*t1 + S(3,6)*S(i1,5)*t1**2 - S(
     &    3,6)*S(i2,5)*t1 + S(3,6)*S(i2,5)*t1**2 )
      qdkbasis5 = qdkbasis5 + ro**2 * (  - 1.D0/2.D0*S(i1,5)*S(i1,6) + 
     &    1.D0/2.D0*S(i1,5)*S(i2,6) - 1.D0/2.D0*S(i1,5)*S(i2,6)*t1 + 1.D
     &    0/2.D0*S(i1,6)*S(i2,5)*t1 )

      qdkbasis6=  + ro * ( 2.D0*S(i1,5)*S(i1,6) - 4.D0*S(i1,5)*S(i1,6)*
     &    t1 + 2.D0*S(i1,5)*S(i1,6)*t1**2 + 2.D0*S(i1,5)*S(i2,6) - 4.D0
     &    *S(i1,5)*S(i2,6)*t1 + 2.D0*S(i1,5)*S(i2,6)*t1**2 - 2.D0*S(i1,
     &    6)*S(i2,5)*t1 + 2.D0*S(i1,6)*S(i2,5)*t1**2 - 2.D0*S(i2,5)*S(
     &    i2,6)*t1 + 2.D0*S(i2,5)*S(i2,6)*t1**2 - 2.D0*S(3,5)*S(i1,6)
     &     + 6.D0*S(3,5)*S(i1,6)*t1**2 - 2.D0*S(3,5)*S(i2,6) + 2.D0*S(3
     &    ,5)*S(i2,6)*t1 + 6.D0*S(3,5)*S(i2,6)*t1**2 - 2.D0*S(3,5)*S(3,
     &    6) + 8.D0*S(3,5)*S(3,6)*t1**2 + 2.D0*S(3,6)*S(i1,5) - 4.D0*S(
     &    3,6)*S(i1,5)*t1 + 2.D0*S(3,6)*S(i1,5)*t1**2 - 2.D0*S(3,6)*S(
     &    i2,5)*t1 + 2.D0*S(3,6)*S(i2,5)*t1**2 )
      qdkbasis6 = qdkbasis6 + ro**2 * ( S(i1,5)*S(i1,6)*t1 + 2.D0*S(i1,
     &    5)*S(i2,6)*t1 + S(i2,5)*S(i2,6)*t1 + 2.D0*S(3,5)*S(i1,6) - S(
     &    3,5)*S(i1,6)*t1 + S(3,5)*S(i2,6) - S(3,5)*S(i2,6)*t1 + 2.D0*
     &    S(3,5)*S(3,6) + S(3,6)*S(i1,5) + S(3,6)*S(i1,5)*t1 + S(3,6)*
     &    S(i2,5)*t1 + 2.D0*S(5,6)*S(i1,i2)*t1 - 2.D0*S(5,6)*S(i1,i2)*
     &    t1**2 )
      qdkbasis6 = qdkbasis6 + ro**3 * (  - 1.D0/2.D0*S(i1,5)*S(i2,6) + 
     &    1.D0/2.D0*S(i1,6)*S(i2,5) - 1.D0/2.D0*S(5,6)*S(i1,i2) )

      return
      end
