      subroutine qbasis(t1,ro,NLO0,NLO1,NLO2,NLO3,NLO4,NLO5,NLO6)
      implicit none
      include 'constants.f'
      include 'masses.f'
      include 'sprods_com.f'
      double precision ro,t1,
     . NLO0,NLO1,NLO2,NLO3,NLO4,NLO5,NLO6
      NLO0 =  + ro * (  - 2.D0*S(1,2)*S(5,6)*t1 + 2.D0*S(1,2)*S(5,6)*
     &    t1**2 - S(1,5)*S(1,6) + S(1,5)*S(2,6) - 2.D0*S(1,5)*S(2,6)*t1
     &     - S(1,5)*S(3,6) - S(1,6)*S(2,5) + 2.D0*S(1,6)*S(2,5)*t1 - S(
     &    1,6)*S(3,5) - S(2,5)*S(2,6) - S(2,5)*S(3,6) - S(2,6)*S(3,5)
     &     - 2.D0*S(3,5)*S(3,6) )
      NLO0 = NLO0 + ro**2 * ( 1.D0/2.D0*S(1,2)*S(5,6) )
      NLO0 = NLO0 - 4.D0*S(1,6)*S(3,5) + 8.D0*S(1,6)*S(3,5)*t1 - 8.D0*
     &    S(1,6)*S(3,5)*t1**2 - 4.D0*S(2,6)*S(3,5) + 8.D0*S(2,6)*S(3,5)
     &    *t1 - 8.D0*S(2,6)*S(3,5)*t1**2 - 4.D0*S(3,5)*S(3,6) + 8.D0*S(
     &    3,5)*S(3,6)*t1 - 8.D0*S(3,5)*S(3,6)*t1**2

      NLO1 =  + ro * (  - 1.D0/2.D0*S(1,2)*S(5,6)*t1 + 3.D0/2.D0*S(1,2)
     &    *S(5,6)*t1**2 - S(1,2)*S(5,6)*t1**3 - 1.D0/4.D0*S(1,5)*S(1,6)
     &     + 1.D0/4.D0*S(1,5)*S(1,6)*t1 + 1.D0/4.D0*S(1,5)*S(2,6) - 5.D0
     &    /4.D0*S(1,5)*S(2,6)*t1 + S(1,5)*S(2,6)*t1**2 - 1.D0/4.D0*S(1,
     &    5)*S(3,6) + 1.D0/4.D0*S(1,5)*S(3,6)*t1 + 3.D0/4.D0*S(1,6)*S(2
     &    ,5)*t1 - S(1,6)*S(2,5)*t1**2 - 1.D0/2.D0*S(1,6)*S(3,5) + 3.D0/
     &    4.D0*S(1,6)*S(3,5)*t1 + 1.D0/4.D0*S(2,5)*S(2,6)*t1 + 1.D0/4.D0
     &    *S(2,5)*S(3,6)*t1 - 1.D0/4.D0*S(2,6)*S(3,5) + 3.D0/4.D0*S(2,6
     &    )*S(3,5)*t1 - 1.D0/2.D0*S(3,5)*S(3,6) + S(3,5)*S(3,6)*t1 )
      NLO1 = NLO1 + ro**2 * ( 1.D0/8.D0*S(1,2)*S(5,6) - 1.D0/4.D0*S(1,2
     &    )*S(5,6)*t1 + 1.D0/8.D0*S(1,5)*S(2,6) - 1.D0/8.D0*S(1,6)*S(2,
     &    5) )
      NLO1 = NLO1 + 2.D0*S(1,6)*S(3,5)*t1 - 6.D0*S(1,6)*S(3,5)*t1**2 + 
     &    4.D0*S(1,6)*S(3,5)*t1**3 + 2.D0*S(2,6)*S(3,5)*t1 - 6.D0*S(2,6
     &    )*S(3,5)*t1**2 + 4.D0*S(2,6)*S(3,5)*t1**3 + 2.D0*S(3,5)*S(3,6
     &    )*t1 - 6.D0*S(3,5)*S(3,6)*t1**2 + 4.D0*S(3,5)*S(3,6)*t1**3

      NLO2 =  + ro * ( 2.D0*S(1,2)*S(5,6)*t1**2 - 2.D0*S(1,2)*S(5,6)*
     &    t1**3 + S(1,5)*S(1,6)*t1 + S(1,5)*S(1,6)*t1**2 - S(1,5)*S(2,6
     &    )*t1 + 3.D0*S(1,5)*S(2,6)*t1**2 + S(1,5)*S(3,6)*t1 + S(1,5)*
     &    S(3,6)*t1**2 - S(1,6)*S(2,5)*t1**2 + 2.D0*S(1,6)*S(3,5)*t1 - 
     &    S(1,6)*S(3,5)*t1**2 + S(2,5)*S(2,6)*t1**2 + S(2,5)*S(3,6)*
     &    t1**2 + S(2,6)*S(3,5)*t1 - S(2,6)*S(3,5)*t1**2 + 2.D0*S(3,5)*
     &    S(3,6)*t1 )
      NLO2 = NLO2 + ro**2 * (  - 1.D0/2.D0*S(1,2)*S(5,6)*t1 - 1.D0/2.D0
     &    *S(1,5)*S(2,6)*t1 + 1.D0/2.D0*S(1,6)*S(2,5)*t1 )
      NLO2 = NLO2 + 8.D0*S(1,6)*S(3,5)*t1**3 + 8.D0*S(2,6)*S(3,5)*t1**3
     &     + 8.D0*S(3,5)*S(3,6)*t1**3

      NLO3 =  + ro * (  - 20.D0*S(1,2)*S(5,6)*t1 + 20.D0*S(1,2)*S(5,6)*
     &    t1**2 - 10.D0*S(1,5)*S(1,6) - 6.D0*S(1,5)*S(1,6)*t1 + 10.D0*
     &    S(1,5)*S(2,6) - 26.D0*S(1,5)*S(2,6)*t1 - 10.D0*S(1,5)*S(3,6)
     &     - 6.D0*S(1,5)*S(3,6)*t1 - 4.D0*S(1,6)*S(2,5) + 14.D0*S(1,6)*
     &    S(2,5)*t1 - 16.D0*S(1,6)*S(3,5) + 6.D0*S(1,6)*S(3,5)*t1 - 4.D0
     &    *S(2,5)*S(2,6) - 6.D0*S(2,5)*S(2,6)*t1 - 4.D0*S(2,5)*S(3,6)
     &     - 6.D0*S(2,5)*S(3,6)*t1 - 10.D0*S(2,6)*S(3,5) + 6.D0*S(2,6)*
     &    S(3,5)*t1 - 20.D0*S(3,5)*S(3,6) )
      NLO3 = NLO3 + ro**2 * ( 5.D0*S(1,2)*S(5,6) + 3.D0*S(1,5)*S(2,6)
     &     - 3.D0*S(1,6)*S(2,5) )
      NLO3 = NLO3 - 16.D0*S(1,6)*S(3,5) + 32.D0*S(1,6)*S(3,5)*t1 - 80.D0
     &    *S(1,6)*S(3,5)*t1**2 - 16.D0*S(2,6)*S(3,5) + 32.D0*S(2,6)*S(3
     &    ,5)*t1 - 80.D0*S(2,6)*S(3,5)*t1**2 - 16.D0*S(3,5)*S(3,6) + 32.
     &    D0*S(3,5)*S(3,6)*t1 - 80.D0*S(3,5)*S(3,6)*t1**2

      NLO4 =  + ro * (  - 1.D0/4.D0*S(1,5)*S(1,6) + 3.D0/4.D0*S(1,5)*S(
     &    1,6)*t1 - 1.D0/2.D0*S(1,5)*S(1,6)*t1**2 - 1.D0/4.D0*S(1,5)*S(
     &    2,6) + 3.D0/4.D0*S(1,5)*S(2,6)*t1 - 1.D0/2.D0*S(1,5)*S(2,6)*
     &    t1**2 - 1.D0/4.D0*S(1,5)*S(3,6) + 3.D0/4.D0*S(1,5)*S(3,6)*t1
     &     - 1.D0/2.D0*S(1,5)*S(3,6)*t1**2 + 1.D0/4.D0*S(1,6)*S(2,5)*t1
     &     - 1.D0/2.D0*S(1,6)*S(2,5)*t1**2 - 1.D0/2.D0*S(1,6)*S(3,5) + 
     &    7.D0/4.D0*S(1,6)*S(3,5)*t1 - 3.D0/2.D0*S(1,6)*S(3,5)*t1**2 + 
     &    1.D0/4.D0*S(2,5)*S(2,6)*t1 - 1.D0/2.D0*S(2,5)*S(2,6)*t1**2 + 
     &    1.D0/4.D0*S(2,5)*S(3,6)*t1 - 1.D0/2.D0*S(2,5)*S(3,6)*t1**2 - 
     &    1.D0/4.D0*S(2,6)*S(3,5) + 5.D0/4.D0*S(2,6)*S(3,5)*t1 - 3.D0/2.
     &    D0*S(2,6)*S(3,5)*t1**2 - 1.D0/2.D0*S(3,5)*S(3,6) + 2.D0*S(3,5
     &    )*S(3,6)*t1 - 2.D0*S(3,5)*S(3,6)*t1**2 )
      NLO4 = NLO4 + ro**2 * (  - 1.D0/2.D0*S(1,2)*S(5,6)*t1 + 1.D0/2.D0
     &    *S(1,2)*S(5,6)*t1**2 - 1.D0/8.D0*S(1,5)*S(1,6) - 1.D0/4.D0*S(
     &    1,5)*S(2,6)*t1 - 1.D0/4.D0*S(1,5)*S(3,6) - 1.D0/4.D0*S(1,6)*
     &    S(2,5) + 1.D0/4.D0*S(1,6)*S(2,5)*t1 - 1.D0/4.D0*S(1,6)*S(3,5)
     &     - 1.D0/8.D0*S(2,5)*S(2,6) - 1.D0/4.D0*S(2,5)*S(3,6) - 1.D0/4.
     &    D0*S(2,6)*S(3,5) - 1.D0/2.D0*S(3,5)*S(3,6) )
      NLO4 = NLO4 + ro**3 * ( 1.D0/8.D0*S(1,2)*S(5,6) )

      NLO5 =  + ro * ( 1.D0/2.D0*S(1,5)*S(1,6) - S(1,5)*S(1,6)*t1 + 1.D0
     &    /2.D0*S(1,5)*S(1,6)*t1**2 + 1.D0/2.D0*S(1,5)*S(2,6) - S(1,5)*
     &    S(2,6)*t1 + 1.D0/2.D0*S(1,5)*S(2,6)*t1**2 + 1.D0/2.D0*S(1,5)*
     &    S(3,6) - S(1,5)*S(3,6)*t1 + 1.D0/2.D0*S(1,5)*S(3,6)*t1**2 - 1.
     &    D0/2.D0*S(1,6)*S(2,5)*t1 + 1.D0/2.D0*S(1,6)*S(2,5)*t1**2 - 1.D
     &    0/2.D0*S(1,6)*S(3,5) - 1.D0/2.D0*S(1,6)*S(3,5)*t1**2 - 1.D0/2.
     &    D0*S(2,5)*S(2,6)*t1 + 1.D0/2.D0*S(2,5)*S(2,6)*t1**2 - 1.D0/2.D
     &    0*S(2,5)*S(3,6)*t1 + 1.D0/2.D0*S(2,5)*S(3,6)*t1**2 - 1.D0/2.D0
     &    *S(2,6)*S(3,5) + 1.D0/2.D0*S(2,6)*S(3,5)*t1 - 1.D0/2.D0*S(2,6
     &    )*S(3,5)*t1**2 - 1.D0/2.D0*S(3,5)*S(3,6) )
      NLO5 = NLO5 + ro**2 * (  - 1.D0/4.D0*S(1,5)*S(1,6) + 1.D0/4.D0*S(
     &    1,5)*S(2,6) - 1.D0/4.D0*S(1,5)*S(2,6)*t1 + 1.D0/4.D0*S(1,6)*
     &    S(2,5)*t1 )

      NLO6 =  + ro * ( S(1,5)*S(1,6) - 2.D0*S(1,5)*S(1,6)*t1 + S(1,5)*
     &    S(1,6)*t1**2 + S(1,5)*S(2,6) - 2.D0*S(1,5)*S(2,6)*t1 + S(1,5)
     &    *S(2,6)*t1**2 + S(1,5)*S(3,6) - 2.D0*S(1,5)*S(3,6)*t1 + S(1,5
     &    )*S(3,6)*t1**2 - S(1,6)*S(2,5)*t1 + S(1,6)*S(2,5)*t1**2 - S(1
     &    ,6)*S(3,5) + 3.D0*S(1,6)*S(3,5)*t1**2 - S(2,5)*S(2,6)*t1 + S(
     &    2,5)*S(2,6)*t1**2 - S(2,5)*S(3,6)*t1 + S(2,5)*S(3,6)*t1**2 - 
     &    S(2,6)*S(3,5) + S(2,6)*S(3,5)*t1 + 3.D0*S(2,6)*S(3,5)*t1**2
     &     - S(3,5)*S(3,6) + 4.D0*S(3,5)*S(3,6)*t1**2 )
      NLO6 = NLO6 + ro**2 * ( S(1,2)*S(5,6)*t1 - S(1,2)*S(5,6)*t1**2 + 
     &    1.D0/2.D0*S(1,5)*S(1,6)*t1 + S(1,5)*S(2,6)*t1 + 1.D0/2.D0*S(1
     &    ,5)*S(3,6) + 1.D0/2.D0*S(1,5)*S(3,6)*t1 + S(1,6)*S(3,5) - 1.D0
     &    /2.D0*S(1,6)*S(3,5)*t1 + 1.D0/2.D0*S(2,5)*S(2,6)*t1 + 1.D0/2.D
     &    0*S(2,5)*S(3,6)*t1 + 1.D0/2.D0*S(2,6)*S(3,5) - 1.D0/2.D0*S(2,
     &    6)*S(3,5)*t1 + S(3,5)*S(3,6) )
      NLO6 = NLO6 + ro**3 * (  - 1.D0/4.D0*S(1,2)*S(5,6) - 1.D0/4.D0*S(
     &    1,5)*S(2,6) + 1.D0/4.D0*S(1,6)*S(2,5) )

      return
      end
