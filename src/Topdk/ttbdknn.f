      msqn(0) = msqn(0) + ro**2*nDn * ( 4.D0*s(j1,4)*s(j1,7)*t1**(-2)
     &     + 8.D0*s(j1,4)*s(j1,7)*t1**(-1) + 4.D0*s(j1,4)*s(j1,7)*
     &    t2**(-2) + 8.D0*s(j1,4)*s(j1,7)*t2**(-1) )
      msqn(0) = msqn(0) + ro**2 * (  - 4.D0*s(4,7)*s(6,3)**2*t1**(-2)
     &     + 8.D0*s(4,7)*s(6,3)*s(6,5)*t1**(-1) + 8.D0*s(4,7)*s(6,3)*s(
     &    6,5)*t2**(-1) - 4.D0*s(4,7)*s(6,5)**2*t2**(-2) - 4.D0*s(6,3)*
     &    s(6,4)*s(j1,7)*t1**(-2) - 4.D0*s(6,3)*s(6,4)*s(j1,7)*t1**(-1)
     &     - 4.D0*s(6,3)*s(6,4)*s(j1,7)*t2**(-1) + 4.D0*s(6,3)*s(6,7)*
