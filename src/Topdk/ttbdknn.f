     &    *s(6,3)*s(6,7)*s(j1,4)*t2**(-1) + 4.D0*s(6,4)*s(6,5)*s(j1,7)*
     &    t1**(-1) + 4.D0*s(6,4)*s(6,5)*s(j1,7)*t2**(-2) + 4.D0*s(6,4)*
     &    s(6,5)*s(j1,7)*t2**(-1) - 4.D0*s(6,5)*s(6,7)*s(j1,4)*t1**(-1)
     &     - 4.D0*s(6,5)*s(6,7)*s(j1,4)*t2**(-2) - 4.D0*s(6,5)*s(6,7)*
     &    s(j1,4)*t2**(-1) )
      msqn(0) = msqn(0) + nDn * ( 64.D0*s(3,4)*s(j1,7) - 32.D0*s(3,4)*
     &    s(j1,7)*t1**(-1) - 32.D0*s(3,4)*s(j1,7)*t2**(-1) + 64.D0*s(3,
     &    4)*s(j2,7) - 32.D0*s(3,4)*s(j2,7)*t1**(-1) - 32.D0*s(3,4)*s(
     &    j2,7)*t2**(-1) + 64.D0*s(3,4)*s(3,7) - 32.D0*s(3,4)*s(3,7)*
     &    t1**(-1) - 32.D0*s(3,4)*s(3,7)*t2**(-1) )

      msqn(0) = msqn(0)/16d0

      msqn(1) =  + V*ro*s12**(-1) * (  - 16.D0*s(3,4)*s(3,7)*s(6,3)**2
     &     - 32.D0*s(3,4)*s(3,7)*s(6,3)*s(6,5) + 32.D0*s(3,4)*s(3,7)*s(
     &    6,3)*s(6,5)*t2**(-1) - 16.D0*s(3,4)*s(3,7)*s(6,5)**2 - 16.D0*
     &    s(3,4)*s(3,7)*s(6,5)**2*t2**(-2) + 32.D0*s(3,4)*s(3,7)*s(6,5)
     &    **2*t2**(-1) - 8.D0*s(3,4)*s(6,3)**2*s(j1,7) - 8.D0*s(3,4)*s(
     &    6,3)**2*s(j1,7)*t2**(-1) - 8.D0*s(3,4)*s(6,3)**2*s(j2,7) - 16.
     &    D0*s(3,4)*s(6,3)*s(6,5)*s(j1,7) + 8.D0*s(3,4)*s(6,3)*s(6,5)*
     &    s(j1,7)*t2**(-2) + 8.D0*s(3,4)*s(6,3)*s(6,5)*s(j1,7)*t2**(-1)
     &     - 16.D0*s(3,4)*s(6,3)*s(6,5)*s(j2,7) + 16.D0*s(3,4)*s(6,3)*
     &    s(6,5)*s(j2,7)*t2**(-1) - 8.D0*s(3,4)*s(6,5)**2*s(j1,7) - 8.D0
     &    *s(3,4)*s(6,5)**2*s(j1,7)*t2**(-2) + 16.D0*s(3,4)*s(6,5)**2*
     &    s(j1,7)*t2**(-1) - 8.D0*s(3,4)*s(6,5)**2*s(j2,7) - 8.D0*s(3,4
     &    )*s(6,5)**2*s(j2,7)*t2**(-2) + 16.D0*s(3,4)*s(6,5)**2*s(j2,7)
     &    *t2**(-1) - 8.D0*s(3,7)*s(6,3)**2*s(j1,4) - 8.D0*s(3,7)*s(6,3
     &    )**2*s(j2,4) - 16.D0*s(3,7)*s(6,3)*s(6,5)*s(j1,4) + 8.D0*s(3,
     &    7)*s(6,3)*s(6,5)*s(j1,4)*t2**(-1) )
      msqn(1) = msqn(1) + V*ro*s12**(-1) * (  - 16.D0*s(3,7)*s(6,3)*s(6
     &    ,5)*s(j2,4) + 16.D0*s(3,7)*s(6,3)*s(6,5)*s(j2,4)*t2**(-1) - 8.
     &    D0*s(3,7)*s(6,5)**2*s(j1,4) + 8.D0*s(3,7)*s(6,5)**2*s(j1,4)*
     &    t2**(-1) - 8.D0*s(3,7)*s(6,5)**2*s(j2,4) - 8.D0*s(3,7)*s(6,5)
     &    **2*s(j2,4)*t2**(-2) + 16.D0*s(3,7)*s(6,5)**2*s(j2,4)*
     &    t2**(-1) - 8.D0*s(6,3)**2*s(j1,4)*s(j1,7) - 8.D0*s(6,3)**2*s(
     &    j1,4)*s(j2,7) - 8.D0*s(6,3)**2*s(j1,7)*s(j2,4) - 8.D0*s(6,3)
     &    **2*s(j2,4)*s(j2,7) - 16.D0*s(6,3)*s(6,5)*s(j1,4)*s(j1,7) + 8.
     &    D0*s(6,3)*s(6,5)*s(j1,4)*s(j1,7)*t2**(-1) - 16.D0*s(6,3)*s(6,
     &    5)*s(j1,4)*s(j2,7) + 8.D0*s(6,3)*s(6,5)*s(j1,4)*s(j2,7)*
     &    t2**(-1) - 16.D0*s(6,3)*s(6,5)*s(j1,7)*s(j2,4) + 16.D0*s(6,3)
     &    *s(6,5)*s(j1,7)*s(j2,4)*t2**(-1) - 16.D0*s(6,3)*s(6,5)*s(j2,4
     &    )*s(j2,7) + 16.D0*s(6,3)*s(6,5)*s(j2,4)*s(j2,7)*t2**(-1) - 8.D
     &    0*s(6,5)**2*s(j1,4)*s(j1,7) + 8.D0*s(6,5)**2*s(j1,4)*s(j1,7)*
     &    t2**(-1) - 8.D0*s(6,5)**2*s(j1,4)*s(j2,7) + 8.D0*s(6,5)**2*s(
     &    j1,4)*s(j2,7)*t2**(-1) )
      msqn(1) = msqn(1) + V*ro*s12**(-1) * (  - 8.D0*s(6,5)**2*s(j1,7)*
     &    s(j2,4) - 8.D0*s(6,5)**2*s(j1,7)*s(j2,4)*t2**(-2) + 16.D0*s(6
     &    ,5)**2*s(j1,7)*s(j2,4)*t2**(-1) - 8.D0*s(6,5)**2*s(j2,4)*s(j2
     &    ,7) - 8.D0*s(6,5)**2*s(j2,4)*s(j2,7)*t2**(-2) + 16.D0*s(6,5)
     &    **2*s(j2,4)*s(j2,7)*t2**(-1) )
      msqn(1) = msqn(1) + V*ro*nDn * ( 8.D0*s(j1,4)*s(j1,7)*t2**(-1) - 
     &    16.D0*s(j1,4)*s(j2,7)*t1 + 8.D0*s(j1,4)*s(j2,7)*t2**(-1) + 16.
     &    D0*s(j1,7)*s(j2,4) + 16.D0*s(j1,7)*s(j2,4)*t1 - 16.D0*s(j1,7)
     &    *s(j2,4)*t2**(-1) + 16.D0*s(1,2)*s(4,7)*t1**2 - 8.D0*s(3,4)*
     &    s(j1,7)*t2**(-2) + 8.D0*s(3,4)*s(j1,7)*t2**(-1) + 8.D0*s(3,7)
     &    *s(j1,4)*t2**(-1) )
      msqn(1) = msqn(1) + V*ro * ( 8.D0*s(3,4)*s(6,3)*s(6,7) - 8.D0*s(3
     &    ,4)*s(6,3)*s(6,7)*t2**(-1) + 8.D0*s(3,4)*s(6,5)*s(6,7) + 8.D0
     &    *s(3,4)*s(6,5)*s(6,7)*t2**(-2) - 16.D0*s(3,4)*s(6,5)*s(6,7)*
     &    t2**(-1) - 8.D0*s(3,7)*s(6,3)*s(6,4) - 8.D0*s(3,7)*s(6,4)*s(6
     &    ,5) + 8.D0*s(3,7)*s(6,4)*s(6,5)*t2**(-1) - 8.D0*s(6,3)*s(6,4)
     &    *s(j1,7) - 8.D0*s(6,3)*s(6,4)*s(j2,7) - 8.D0*s(6,4)*s(6,5)*s(
